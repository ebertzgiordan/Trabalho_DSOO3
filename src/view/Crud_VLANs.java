    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // botao salvar
        try {
            int idVlan = Integer.parseInt(jTextField1.getText().trim());
            String nome = jTextField2.getText().trim();

            Vlan vlan = new Vlan(0, idVlan, nome);
            VlanController controller = new VlanController();
            controller.salvar(vlan);

            JOptionPane.showMessageDialog(this, "VLAN salva com sucesso!");
            atualizarTabela();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //botao editar
        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma VLAN para editar.");
            return;
        }

        try {
            int id = (int) jTable1.getValueAt(linha, 0);
            int idVlan = Integer.parseInt(jTextField1.getText().trim());
            String nome = jTextField2.getText().trim();

            Vlan v = new Vlan(id, idVlan, nome);
            VlanDAO dao = new VlanDAO();
            dao.atualizar(v);

            JOptionPane.showMessageDialog(this, "VLAN atualizada!");
            atualizarTabela();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //botao excluir
        int linha = jTable1.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma VLAN para excluir.");
            return;
        }

        int confirmar = JOptionPane.showConfirmDialog(this, "Tem certeza?", "Confirma√ß√£o", JOptionPane.YES_NO_OPTION);
        if (confirmar != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            int id = (int) jTable1.getValueAt(linha, 0);
            VlanDAO dao = new VlanDAO();
            dao.excluir(id);
            JOptionPane.showMessageDialog(this, "VLAN exclu√≠da.");
            atualizarTabela();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

